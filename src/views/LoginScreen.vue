<template>
  <div class="login-screen min-h-screen flex items-center justify-center bg-gray-50 dark:bg-gray-900">
    <div class="max-w-md w-full space-y-8 p-8">
      <!-- Logo and Title -->
      <div class="text-center">
        <div class="mx-auto h-20 w-20 bg-primary-600 rounded-xl flex items-center justify-center mb-6">
          <svg class="w-10 h-10 text-white" fill="none" stroke="currentColor" viewBox="0 0 24 24">
            <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M9 5H7a2 2 0 00-2 2v10a2 2 0 002 2h8a2 2 0 002-2V7a2 2 0 00-2-2h-2M9 5a2 2 0 002 2h2a2 2 0 002-2M9 5a2 2 0 012-2h2a2 2 0 012 2"></path>
          </svg>
        </div>
        <h2 class="text-3xl font-bold text-gray-900 dark:text-white">Apex Task Manager</h2>
        <p class="mt-2 text-sm text-gray-600 dark:text-gray-400">
          AI-powered task management for multi-business operations
        </p>
      </div>

      <!-- Error Message -->
      <div v-if="error" class="bg-red-50 dark:bg-red-900 border border-red-200 dark:border-red-700 rounded-md p-4">
        <div class="flex">
          <div class="flex-shrink-0">
            <svg class="h-5 w-5 text-red-400" fill="none" stroke="currentColor" viewBox="0 0 24 24">
              <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M12 9v2m0 4h.01m-6.938 4h13.856c1.54 0 2.502-1.667 1.732-2.5L13.732 4c-.77-.833-1.964-.833-2.732 0L3.732 16c-.77.833.192 2.5 1.732 2.5z"></path>
            </svg>
          </div>
          <div class="ml-3">
            <p class="text-sm text-red-700 dark:text-red-200">{{ error }}</p>
          </div>
        </div>
      </div>

      <!-- Success Message -->
      <div v-if="message" class="bg-green-50 dark:bg-green-900 border border-green-200 dark:border-green-700 rounded-md p-4">
        <div class="flex">
          <div class="flex-shrink-0">
            <svg class="h-5 w-5 text-green-400" fill="none" stroke="currentColor" viewBox="0 0 24 24">
              <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M9 12l2 2 4-4m6 2a9 9 0 11-18 0 9 9 0 0118 0z"></path>
            </svg>
          </div>
          <div class="ml-3">
            <p class="text-sm text-green-700 dark:text-green-200">{{ message }}</p>
          </div>
        </div>
      </div>

      <!-- Login Form -->
      <div class="card">
        <div class="space-y-6">
          <!-- Google Sign In -->
          <div>
            <button
              @click="signInWithGoogle"
              :disabled="loading"
              class="w-full flex justify-center items-center px-4 py-2 border border-gray-300 dark:border-gray-600 rounded-md shadow-sm bg-white dark:bg-gray-800 text-sm font-medium text-gray-700 dark:text-gray-300 hover:bg-gray-50 dark:hover:bg-gray-700 focus:outline-none focus:ring-2 focus:ring-offset-2 focus:ring-primary-500 disabled:opacity-50 disabled:cursor-not-allowed"
            >
              <svg v-if="!loading" class="w-5 h-5 mr-3" viewBox="0 0 24 24">
                <path fill="#4285F4" d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"/>
                <path fill="#34A853" d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"/>
                <path fill="#FBBC05" d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"/>
                <path fill="#EA4335" d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"/>
              </svg>
              <div v-else class="w-5 h-5 mr-3">
                <div class="animate-spin rounded-full h-5 w-5 border-b-2 border-primary-600"></div>
              </div>
              <span v-if="!loading">Continue with Google</span>
              <span v-else>Signing in...</span>
            </button>
          </div>

          <!-- Divider -->
          <div class="relative">
            <div class="absolute inset-0 flex items-center">
              <div class="w-full border-t border-gray-300 dark:border-gray-600"></div>
            </div>
            <div class="relative flex justify-center text-sm">
              <span class="px-2 bg-white dark:bg-gray-800 text-gray-500 dark:text-gray-400">Or continue with email</span>
            </div>
          </div>

          <!-- Email/Password Form -->
          <form @submit.prevent="handleSubmit" class="space-y-6">
            <div>
              <label for="email" class="block text-sm font-medium text-gray-700 dark:text-gray-300 mb-2">
                Email address
              </label>
              <input
                id="email"
                v-model="form.email"
                type="email"
                required
                class="input-field"
                placeholder="Enter your email"
              />
            </div>

            <div>
              <label for="password" class="block text-sm font-medium text-gray-700 dark:text-gray-300 mb-2">
                Password
              </label>
              <div class="relative">
                <input
                  id="password"
                  v-model="form.password"
                  :type="showPassword ? 'text' : 'password'"
                  required
                  class="input-field pr-10"
                  placeholder="Enter your password"
                />
                <button
                  type="button"
                  @click="showPassword = !showPassword"
                  class="absolute inset-y-0 right-0 pr-3 flex items-center text-gray-400 hover:text-gray-600 dark:hover:text-gray-300"
                >
                  <svg v-if="showPassword" class="h-5 w-5" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                    <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M15 12a3 3 0 11-6 0 3 3 0 016 0z"></path>
                    <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M2.458 12C3.732 7.943 7.523 5 12 5c4.478 0 8.268 2.943 9.542 7-1.274 4.057-5.064 7-9.542 7-4.477 0-8.268-2.943-9.542-7z"></path>
                  </svg>
                  <svg v-else class="h-5 w-5" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                    <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M13.875 18.825A10.05 10.05 0 0112 19c-4.478 0-8.268-2.943-9.543-7a9.97 9.97 0 011.563-3.029m5.858.908a3 3 0 114.243 4.243M9.878 9.878l4.242 4.242M9.878 9.878L3 3m6.878 6.878L21 21"></path>
                  </svg>
                </button>
              </div>
            </div>

            <div class="flex items-center justify-between">
              <div class="flex items-center">
                <input
                  id="remember-me"
                  v-model="form.rememberMe"
                  type="checkbox"
                  class="h-4 w-4 text-primary-600 focus:ring-primary-500 border-gray-300 rounded"
                />
                <label for="remember-me" class="ml-2 block text-sm text-gray-700 dark:text-gray-300">
                  Remember me
                </label>
              </div>

              <button
                type="button"
                @click="resetPassword"
                class="text-sm text-primary-600 dark:text-primary-400 hover:text-primary-800 dark:hover:text-primary-200"
              >
                Forgot password?
              </button>
            </div>

            <div>
              <button
                type="submit"
                :disabled="loading"
                class="btn-primary w-full"
              >
                <span v-if="!loading">{{ isSignUp ? 'Create Account' : 'Sign In' }}</span>
                <span v-else>{{ isSignUp ? 'Creating Account...' : 'Signing In...' }}</span>
              </button>
            </div>
          </form>

          <!-- Toggle Sign Up/Sign In -->
          <div class="text-center">
            <button
              @click="isSignUp = !isSignUp"
              class="text-sm text-primary-600 dark:text-primary-400 hover:text-primary-800 dark:hover:text-primary-200"
            >
              {{ isSignUp ? 'Already have an account? Sign in' : 'Need an account? Sign up' }}
            </button>
          </div>

          <!-- Override Code Access -->
          <div class="text-center pt-4 border-t border-gray-200 dark:border-gray-700">
            <button
              @click="showOverrideModal = true"
              class="text-xs text-gray-500 dark:text-gray-400 hover:text-gray-700 dark:hover:text-gray-300"
            >
              Emergency Access
            </button>
          </div>
        </div>
      </div>

      <!-- Override Code Modal -->
      <div v-if="showOverrideModal" class="fixed inset-0 bg-gray-600 bg-opacity-50 overflow-y-auto h-full w-full z-50">
        <div class="relative top-20 mx-auto p-5 border w-96 shadow-lg rounded-md bg-white dark:bg-gray-800">
          <div class="mt-3">
            <h3 class="text-lg font-medium text-gray-900 dark:text-white mb-4">Emergency Override</h3>
            <p class="text-sm text-gray-600 dark:text-gray-400 mb-4">
              Enter your override code for emergency access.
            </p>
            
            <input
              v-model="overrideCode"
              type="password"
              placeholder="Enter override code"
              class="input-field mb-4"
              @keyup.enter="handleOverride"
            />
            
            <div class="flex justify-end space-x-3">
              <button @click="showOverrideModal = false" class="btn-secondary">
                Cancel
              </button>
              <button @click="handleOverride" class="btn-primary">
                Access
              </button>
            </div>
          </div>
        </div>
      </div>
    </div>
  </div>
</template>

<script setup>
import { ref, reactive } from 'vue'
import { useRouter } from 'vue-router'
import { useAuthStore } from '../stores/authStore'

const router = useRouter()
const authStore = useAuthStore()

const loading = ref(false)
const error = ref('')
const message = ref('')
const isSignUp = ref(false)
const showPassword = ref(false)
const showOverrideModal = ref(false)
const overrideCode = ref('')

const form = reactive({
  email: '',
  password: '',
  rememberMe: false
})

const signInWithGoogle = async () => {
  loading.value = true
  error.value = ''
  try {
    await authStore.signInWithGoogle()
    router.push('/dashboard')
  } catch (err) {
    error.value = err.message || 'Failed to sign in with Google'
  } finally {
    loading.value = false
  }
}

const handleSubmit = async () => {
  loading.value = true
  error.value = ''
  
  try {
    if (isSignUp.value) {
      await authStore.signUp(form.email, form.password)
      message.value = 'Account created successfully! Please check your email for verification.'
      isSignUp.value = false
    } else {
      await authStore.signIn(form.email, form.password)
      router.push('/dashboard')
    }
  } catch (err) {
    error.value = err.message || 'Authentication failed'
  } finally {
    loading.value = false
  }
}

const resetPassword = async () => {
  if (!form.email) {
    error.value = 'Please enter your email address first'
    return
  }
  
  try {
    await authStore.resetPassword(form.email)
    message.value = 'Password reset email sent! Check your inbox.'
    error.value = ''
  } catch (err) {
    error.value = err.message || 'Failed to send password reset email'
  }
}

const handleOverride = () => {
  // Check override codes (123, 456, 789)
  const validCodes = ['123', '456', '789']
  if (validCodes.includes(overrideCode.value)) {
    // Create temporary emergency access
    authStore.setEmergencyAccess(overrideCode.value)
    showOverrideModal.value = false
    router.push('/dashboard')
  } else {
    error.value = 'Invalid override code'
    showOverrideModal.value = false
    overrideCode.value = ''
  }
}
</script>